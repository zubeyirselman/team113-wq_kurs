package day11_StringManipulations_ForLoop;

public class c08_Loops {
    public static void main(String[] args) {

        // 10 kere "Java Candir" yazdirin.

        System.out.println("Java Candir");

        for (int i = 1; i <= 10 ; i++) {
            System.out.println(i + "-Java Candir");
        }



    }
}
